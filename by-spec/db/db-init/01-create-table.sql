CREATE TABLE Transaction (
    id INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    date DATE NOT NULL,
    status INT NOT NULL,
    amount INT NOT NULL,
    guid UUID NOT NULL,
    message JSONB NOT NULL
) PARTITION BY RANGE (date);

CREATE TABLE Transaction_2025_oct PARTITION OF TRANSACTION
    FOR VALUES FROM ('2025-10-01') TO ('2025-11-01');

CREATE TABLE Transaction_2025_nov PARTITION OF TRANSACTION
    FOR VALUES FROM ('2025-11-01') TO ('2025-12-01');

CREATE TABLE Transaction_2025_dec PARTITION OF TRANSACTION
    FOR VALUES FROM ('2025-12-01') TO ('2026-01-01');

CREATE TABLE Transaction_2026_jan PARTITION OF TRANSACTION
    FOR VALUES FROM ('2026-01-01') TO ('2026-02-01');